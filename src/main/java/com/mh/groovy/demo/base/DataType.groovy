package com.mh.groovy.demo.base

class DataType {
	
	static void main(String[] args) {
		byte by = 'a';
		char c = 'a';
		short sh = 10;
		int i = 5;
		float f = 2.34f;
		double d = 2.45;
		long l = 24L;
		BigDecimal bd = 3.5g;
		BigInteger bi = 30g;
		boolean b = true;
		String s = "hello jason";
		
		println(by); // 97
		println(c); // a
		println(sh); // 10
		println(i); // 5
		println(f); // 2.34
		println(d); // 2.45
		println(l); // 24
		println(bd); // 3.5
		println(bi); // 30
		println(b); // true
		println(s); // hello jason
	}
	
}
